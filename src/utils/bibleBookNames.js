/**
 * Get the localized name for a book given its BIBLE_BOOKS index and language code.
 * Falls back to English if the language or index is not found.
 */
export function getLocalizedBookName(bookIndex, langCode) {
  const names = BOOK_NAMES_BY_LANG[langCode] || EN_BOOKS
  return names[bookIndex] || EN_BOOKS[bookIndex] || ''
}

/**
 * Get localized book name by book ID (e.g. 'GEN', 'MAT').
 */
export function getLocalizedBookNameById(bookId, langCode) {
  const idx = BOOK_IDS.indexOf(bookId)
  if (idx === -1) return bookId
  return getLocalizedBookName(idx, langCode)
}
