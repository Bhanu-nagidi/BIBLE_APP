function generateFlatChapters(booksList) {
  const list = [];
  booksList.forEach(book => {
    for (let c = 1; c <= book.chapters; c++) {
      list.push({ bookId: book.id, bookName: book.name, chapter: c });
    }
  });
  return list;
}

function distributeChapters(flatChapters, duration) {
  const days = [];
  const total = flatChapters.length;
  for (let d = 1; d <= duration; d++) {
    const startIdx = Math.floor((d - 1) * total / duration);
    const endIdx = Math.floor(d * total / duration) - 1;
    
    const readings = [];
    let currentReading = null;
    
    for (let i = startIdx; i <= endIdx; i++) {
      if (i >= total) break;
      const ch = flatChapters[i];
      if (!currentReading || currentReading.bookId !== ch.bookId) {
        if (currentReading) readings.push(currentReading);
        currentReading = {
          bookId: ch.bookId,
          bookName: ch.bookName,
          startChapter: ch.chapter,
          endChapter: ch.chapter
        };
      } else {
        currentReading.endChapter = ch.chapter;
      }
    }
    if (currentReading) readings.push(currentReading);
    
    days.push({ day: d, readings, isRestDay: false });
  }
  return days;
}

function generateOTNTMix(duration) {
  const otBooks = BIBLE_BOOKS_CH.filter(b => b.testament === 'OT');
  const ntBooks = BIBLE_BOOKS_CH.filter(b => b.testament === 'NT');
  const otFlat = generateFlatChapters(otBooks);
  const ntFlat = generateFlatChapters(ntBooks);
  
  const otDays = distributeChapters(otFlat, duration);
  const ntDays = distributeChapters(ntFlat, duration);
  
  return otDays.map((otDay, idx) => {
    const ntDay = ntDays[idx];
    return {
      day: otDay.day,
      readings: [
        ...otDay.readings.map(r => ({ ...r, label: 'Old Testament' })),
        ...(ntDay ? ntDay.readings.map(r => ({ ...r, label: 'New Testament' })) : [])
      ],
      isRestDay: false
    };
  });
}

function generateBookByBook(duration) {
  const flat = generateFlatChapters(BIBLE_BOOKS_CH);
  return distributeChapters(flat, duration);
}

function generateMorningEvening(duration) {
  const ntBooks = BIBLE_BOOKS_CH.filter(b => b.testament === 'NT');
  const psalmsBook = BIBLE_BOOKS_CH.find(b => b.id === 'PSA');
  const morningFlat = [
    ...generateFlatChapters(ntBooks),
    ...generateFlatChapters([psalmsBook])
  ];
  
  const otBooksExPsalms = BIBLE_BOOKS_CH.filter(b => b.testament === 'OT' && b.id !== 'PSA');
  const eveningFlat = generateFlatChapters(otBooksExPsalms);
  
  const morningDays = distributeChapters(morningFlat, duration);
  const eveningDays = distributeChapters(eveningFlat, duration);
  
  return morningDays.map((mDay, idx) => {
    const eDay = eveningDays[idx];
    return {
      day: mDay.day,
      readings: [
        ...mDay.readings.map(r => ({ ...r, label: 'Morning' })),
        ...(eDay ? eDay.readings.map(r => ({ ...r, label: 'Evening' })) : [])
      ],
      isRestDay: false
    };
  });
}

function generate5DayPlan(readingDuration) {
  const flat = generateFlatChapters(BIBLE_BOOKS_CH);
  const readingDays = distributeChapters(flat, readingDuration);
  
  const totalWeeks = Math.ceil(readingDuration / 5);
  const fullPlan = [];
  let readingDayIndex = 0;
  
  for (let w = 0; w < totalWeeks; w++) {
    for (let d = 1; d <= 7; d++) {
      const dayNum = w * 7 + d;
      if (d === 6 || d === 7) {
        fullPlan.push({
          day: dayNum,
          readings: [],
          isRestDay: true,
          restLabel: d === 6 ? 'Saturday Reflection & Catch Up' : 'Sunday Rest & Worship'
        });
      } else {
        if (readingDayIndex < readingDays.length) {
          const rDay = readingDays[readingDayIndex++];
          fullPlan.push({
            day: dayNum,
            readings: rDay.readings,
            isRestDay: false
          });
        } else {
          fullPlan.push({
            day: dayNum,
            readings: [],
            isRestDay: true,
            restLabel: 'Reflection & Catch Up'
          });
        }
      }
    }
  }
  return fullPlan;
}

function generateNTFirst(duration) {
  const ntBooks = BIBLE_BOOKS_CH.filter(b => b.testament === 'NT');
  const otBooks = BIBLE_BOOKS_CH.filter(b => b.testament === 'OT');
  
  const ntFlat = generateFlatChapters(ntBooks);
  const otFlat = generateFlatChapters(otBooks);
  
  const ntDuration = Math.max(1, Math.round(duration * 0.3));
  const otDuration = duration - ntDuration;
  
  const ntDays = distributeChapters(ntFlat, ntDuration);
  const otDays = distributeChapters(otFlat, otDuration);
  
  const fullPlan = [];
  ntDays.forEach((d, i) => {
    fullPlan.push({
      day: i + 1,
      readings: d.readings.map(r => ({ ...r, label: 'New Testament' })),
      isRestDay: false
    });
  });
  otDays.forEach((d, i) => {
    fullPlan.push({
      day: ntDuration + i + 1,
      readings: d.readings.map(r => ({ ...r, label: 'Old Testament' })),
      isRestDay: false
    });
  });
  return fullPlan;
}

function generatePsalmsProverbsRest(duration) {
  const restBooks = BIBLE_BOOKS_CH.filter(b => b.id !== 'PSA' && b.id !== 'PRO');
  const restFlat = generateFlatChapters(restBooks);
  const restDays = distributeChapters(restFlat, duration);
  
  return restDays.map((rDay, idx) => {
    const day = rDay.day;
    const pslCh = ((day - 1) % 150) + 1;
    const prvCh = ((day - 1) % 31) + 1;
    
    return {
      day,
      readings: [
        { bookId: 'PSA', bookName: 'Psalms', startChapter: pslCh, endChapter: pslCh, label: 'Daily Psalm' },
        { bookId: 'PRO', bookName: 'Proverbs', startChapter: prvCh, endChapter: prvCh, label: 'Daily Proverbs' },
        ...rDay.readings.map(r => ({ ...r, label: 'Regular' }))
      ],
      isRestDay: false
    };
  });
}

function generateGospelsParallel(duration) {
  const gDuration = Math.max(1, Math.round(duration * 0.25));
  const restDuration = duration - gDuration;
  
  const gPlan = [];
  for (let i = 0; i < gDuration; i++) {
    const dayNum = i + 1;
    const readings = [];
    
    const matStart = Math.floor(i * 28 / gDuration) + 1;
    const matEnd = Math.floor((i + 1) * 28 / gDuration);
    if (matEnd >= matStart) readings.push({ bookId: 'MAT', bookName: 'Matthew', startChapter: matStart, endChapter: matEnd, label: 'Gospels Parallel' });
    
    const mrkStart = Math.floor(i * 16 / gDuration) + 1;
    const mrkEnd = Math.floor((i + 1) * 16 / gDuration);
    if (mrkEnd >= mrkStart) readings.push({ bookId: 'MRK', bookName: 'Mark', startChapter: mrkStart, endChapter: mrkEnd, label: 'Gospels Parallel' });
    
    const lukStart = Math.floor(i * 24 / gDuration) + 1;
    const lukEnd = Math.floor((i + 1) * 24 / gDuration);
    if (lukEnd >= lukStart) readings.push({ bookId: 'LUK', bookName: 'Luke', startChapter: lukStart, endChapter: lukEnd, label: 'Gospels Parallel' });
    
    const jhnStart = Math.floor(i * 21 / gDuration) + 1;
    const jhnEnd = Math.floor((i + 1) * 21 / gDuration);
    if (jhnEnd >= jhnStart) readings.push({ bookId: 'JHN', bookName: 'John', startChapter: jhnStart, endChapter: jhnEnd, label: 'Gospels Parallel' });
    
    gPlan.push({ day: dayNum, readings, isRestDay: false });
  }
  
  const restBooks = BIBLE_BOOKS_CH.filter(b => !['MAT', 'MRK', 'LUK', 'JHN'].includes(b.id));
  const restFlat = generateFlatChapters(restBooks);
  const restDays = distributeChapters(restFlat, restDuration);
  
  const fullPlan = [...gPlan];
  restDays.forEach((rDay, idx) => {
    fullPlan.push({
      day: gDuration + rDay.day,
      readings: rDay.readings.map(r => ({ ...r, label: 'Rest of Bible' })),
      isRestDay: false
    });
  });
  return fullPlan;
}

function generateLanguageRotation(duration) {
  const flat = generateFlatChapters(BIBLE_BOOKS_CH);
  const days = distributeChapters(flat, duration);
  return days.map(d => ({
    ...d,
    languageCode: d.day % 2 === 1 ? 'te' : 'en',
    languageLabel: d.day % 2 === 1 ? 'Telugu (తెలుగు)' : 'English'
  }));
}

