export function generateReadingPlan(planId, duration) {
  const actualDuration = planId === 'topical' ? 30 : duration;
  switch (planId) {
    case 'chronological':
      const chronoFlat = generateFlatChapters(
        CHRONOLOGICAL_ORDER.map(id => BIBLE_BOOKS_CH.find(b => b.id === id)).filter(Boolean)
      );
      return distributeChapters(chronoFlat, actualDuration);
    case 'ot_nt_mix':
      return generateOTNTMix(actualDuration);
    case 'book_by_book':
      return generateBookByBook(actualDuration);
    case 'morning_evening':
      return generateMorningEvening(actualDuration);
    case '5_day_plan':
      return generate5DayPlan(actualDuration);
    case 'nt_first':
      return generateNTFirst(actualDuration);
    case 'psalms_proverbs':
      return generatePsalmsProverbsRest(actualDuration);
    case 'gospels_parallel':
      return generateGospelsParallel(actualDuration);
    case 'language_rotation':
      return generateLanguageRotation(actualDuration);
    case 'topical':
    default:
      return TOPICAL_THEMES.map((theme, i) => ({
        day: i + 1,
        theme: theme.theme,
        readings: theme.passages,
        isRestDay: false
      }));
  }
}
